#include <stdio.h>

/* NOTE: Ordinarily, the arguments to main are the command line arguments. In
 *       this case, we do not need any additional information, so we can list
 *       the parameters as "void" instead to reassure the compiler that we are
 *       not forgetting to use the command line arguments. */
int main(void) {
    printf("Hello, world!\n");

    return 0;
}
